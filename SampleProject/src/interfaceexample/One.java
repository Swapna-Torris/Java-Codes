package interfaceexample;

public class One {

	One(){
		System.out.println("One class constructor");
	}
	public static void display() {
		System.out.println("this is static display method in One class");
	}
	
	protected void display1() {
		System.out.println("this is display method in One class");
	}
	
	protected void display1(int a) {
		System.out.println("this is display method in One class");
	}
}
